import streamlit as st
import pandas as pd

# Load the dataset
house_data = pd.read_csv("/content/test.csv")

# Filter the dataset to start with the house id 1461
house_data = house_data[house_data.index >= 1461]

# Reset the index
house_data = house_data.reset_index(drop=True)

st.title('House Price Prediction App')

# Function to gather user input
def user_input_features():
    id = st.number_input('House ID', min_value=0, max_value=len(house_data)-1, value=0)
    saleprice = st.number_input('Sale Price', min_value=0.0, value=121390.0)

    # Create a dictionary of the input features
    data = {
        'Id': house_data.iloc[id]['Id'],
        'SalePrice': saleprice
    }

    features = pd.DataFrame(data, index=[0])
    return features

# Display user input features
input_df = user_input_features()
st.write('Specified Input parameters')
st.write(input_df)

# Predict button to display the corresponding house details
if st.button('Predict'):
    house_id = input_df['Id'].values[0]
    sale_price = input_df['SalePrice'].values[0]

    house_details = house_data[house_data['Id'] == house_id]
    st.write(f"Details for House ID {house_id} with Sale Price ${sale_price:,.2f}:")
    st.write(house_details)